package com.dndbean;
import java.io.*;


public class CircleSummaryBean
{

        private String circle;
        private String dateval;
        private String totalcall;
        private String callallowed;
        private String callblocked;

        public String getCircle() {
                return circle;
        }

        public void setCircle( String circle ) {
                this.circle = circle;
        }

        public String getDateval() {
                return dateval;
        }

        public void setDateval( String dateval ) {
                this.dateval = dateval;
        }

        public String getTotalcall() {
                return totalcall;
        }

        public void setTotalcall( String totalcall ) {
                this.totalcall = totalcall;
        }

        public String getCallallowed() {
                return callallowed;
        }

        public void setCallallowed( String callallowed ) {
                this.callallowed = callallowed;
        }

        public String getCallblocked() {
                return callblocked;
        }

        public void setCallblocked( String callblocked ) {
                this.callblocked = callblocked;
        }

}
