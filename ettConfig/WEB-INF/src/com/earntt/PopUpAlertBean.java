package com.earntt;
public class PopUpAlertBean {
	private String id;
	private String status;
	private String heading;
	private String text;
	private String noOfButton;
	private String buttonsText;
	private String actinoUrl;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getHeading() {
		return heading;
	}
	public void setHeading(String heading) {
		this.heading = heading;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getNoOfButton() {
		return noOfButton;
	}
	public void setNoOfButton(String noOfButton) {
		this.noOfButton = noOfButton;
	}
	public String getButtonsText() {
		return buttonsText;
	}
	public void setButtonsText(String buttonsText) {
		this.buttonsText = buttonsText;
	}
	public String getActinoUrl() {
		return actinoUrl;
	}
	public void setActinoUrl(String actinoUrl) {
		this.actinoUrl = actinoUrl;
	}
	
	

}

