package com.domain.entity;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity
public class RedeemAmountConfig implements Serializable{

		@Id
		@GeneratedValue(strategy = GenerationType.AUTO)
		private long id;
		
		private String operator;
		
		private String amount;
		
		private String IstRedeemAmount;
		
		private boolean status;
		
		private String type;
		
		private String fee;
		private String threshold;
		private String IstThreshold;
		public String getOperator() {
			return operator;
		}

		public void setOperator(String operator) {
			this.operator = operator;
		}

		public String getAmount() {
			return amount;
		}

		public void setAmount(String amount) {
			this.amount = amount;
		}

		public boolean isStatus() {
			return status;
		}

		public void setStatus(boolean status) {
			this.status = status;
		}

		public long getId() {
			return id;
		}

		public void setId(long id) {
			this.id = id;
		}

		public String getType() {
			return type;
		}

		public void setType(String type) {
			this.type = type;
		}

		public String getFee() {
			return fee;
		}

		public void setFee(String fee) {
			this.fee = fee;
		}

		public String getIstRedeemAmount() {
			return IstRedeemAmount;
		}

		public void setIstRedeemAmount(String istRedeemAmount) {
			IstRedeemAmount = istRedeemAmount;
		}

		public String getThreshold() {
			return threshold;
		}

		public void setThreshold(String threshold) {
			this.threshold = threshold;
		}

		public String getIstThreshold() {
			return IstThreshold;
		}

		public void setIstThreshold(String istThreshold) {
			IstThreshold = istThreshold;
		}
		
		
		
}
