package com.domain.entity;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class UserAccount implements Serializable {

	@Id
	private Long ettId;

	private float currentBalance;
	
	private int balanceCoins;
	
	private int balanceBonus;
	
	

	public int getBalanceCoins() {
		return balanceCoins;
	}

	public void setBalanceCoins(int balanceCoins) {
		this.balanceCoins = balanceCoins;
	}

	public Long getEttId() {
		return ettId;
	}

	public void setEttId(Long ettId) {
		this.ettId = ettId;
	}

	public float getCurrentBalance() {
		return currentBalance;
	}

	public void setCurrentBalance(float currentBalance) {
		this.currentBalance = currentBalance;
	}

	public int getBalanceBonus() {
		return balanceBonus;
	}

	public void setBalanceBonus(int balanceBonus) {
		this.balanceBonus = balanceBonus;
	}
	
	
}