package com.web.rest.dto;

import com.fasterxml.jackson.databind.annotation.JsonSerialize;

@JsonSerialize(include = JsonSerialize.Inclusion.ALWAYS)
public class DataUsagePushDto {

	private long id;
	private String packageName;
	private String appKey;
	private int timeInterval;
	private int dataThreshold;
	private int eligibleDays;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public String getAppKey() {
		return appKey;
	}
	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}
	public int getTimeInterval() {
		return timeInterval;
	}
	public void setTimeInterval(int timeInterval) {
		this.timeInterval = timeInterval;
	}
	public int getDataThreshold() {
		return dataThreshold;
	}
	public void setDataThreshold(int dataThreshold) {
		this.dataThreshold = dataThreshold;
	}
	public int getEligibleDays() {
		return eligibleDays;
	}
	public void setEligibleDays(int eligibleDays) {
		this.eligibleDays = eligibleDays;
	}
	
	
}
