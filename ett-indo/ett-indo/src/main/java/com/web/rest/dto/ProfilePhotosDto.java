package com.web.rest.dto;




import com.fasterxml.jackson.databind.annotation.JsonSerialize;

/**
 * @author Ankur
 */
//TODO:: status must have with tags or message
@JsonSerialize(include = JsonSerialize.Inclusion.ALWAYS)
public class ProfilePhotosDto {
	
    private Long objectId;
    private String srcBig;
	private Integer srcBigHeight;
	private Integer srcBigWidth;
	private boolean isProfilePic;
	public Long getObjectId() {
		return objectId;
	}
	public void setObjectId(Long objectId) {
		this.objectId = objectId;
	}
	public String getSrcBig() {
		return srcBig;
	}
	public void setSrcBig(String srcBig) {
		this.srcBig = srcBig;
	}
	public Integer getSrcBigHeight() {
		return srcBigHeight;
	}
	public void setSrcBigHeight(Integer srcBigHeight) {
		this.srcBigHeight = srcBigHeight;
	}
	public Integer getSrcBigWidth() {
		return srcBigWidth;
	}
	public void setSrcBigWidth(Integer srcBigWidth) {
		this.srcBigWidth = srcBigWidth;
	}
	public boolean isProfilePic() {
		return isProfilePic;
	}
	public void setProfilePic(boolean isProfilePic) {
		this.isProfilePic = isProfilePic;
	}
	
}
